def clean_report_output(text: str) -> str:
    t = html.unescape((text or "").strip())
    # Reports are copied into parent chats as plain text. Strip Telegram/HTML and
    # Markdown markup so Mini App and Telegram do not show raw <b>...</b>.
    t = re.sub(r"<br\s*/?>", "\n", t, flags=re.IGNORECASE)
    t = re.sub(r"</p\s*>", "\n", t, flags=re.IGNORECASE)
    t = re.sub(r"</?(?:b|i|u|strong|em|code|span|p)[^>]*>", "", t, flags=re.IGNORECASE)
    t = re.sub(r"<[^>]+>", "", t)
    t = t.replace("**", "").replace("__", "").replace("`", "")
    junk_prefixes = [
        "Конечно, вот", "Вот готовый", "Готовый отчёт", "Готовый отчет", "Вариант отчёта", "Вариант отчета",
        "Хорошо, вот", "Можно отправить так:", "Текст отчёта:", "Текст отчета:",
    ]
    for p in junk_prefixes:
        if t.lower().startswith(p.lower()):
            lines = t.splitlines()
            if len(lines) > 1:
                t = "\n".join(lines[1:]).strip()
            else:
                t = t[len(p):].strip(" :—-\n")
    t = t.replace("---", "").strip()
    t = re.sub(r"[ \t]+\n", "\n", t)
    t = re.sub(r"\n{3,}", "\n\n", t)
    if "здравствуйте" not in normalize(t[:160]):
        t = "Здравствуйте, уважаемые родители!\n\n" + t
    return trim(t, 3600)


def _find_course_key(text: str) -> Optional[str]:
    n = _canonical_report_query(text)
    # Keep the old structured fallback aliases, but normalize input more robustly.
    for alias, key in sorted(COURSE_ALIASES.items(), key=lambda x: len(x[0]), reverse=True):
        a = _canonical_report_query(alias)
        if not a:
            continue
        if len(a) <= 3 or re.fullmatch(r"[a-z0-9+#]+", a):
            if re.search(rf"(?<![a-zа-я0-9]){re.escape(a)}(?![a-zа-я0-9])", n):
                return key
        elif a in n:
            return key
    keys = extract_course_keys(n)
    if not keys:
        return None
    # Structured fallback has more specific keys for some broad tracks.
    broad_map = {
        "blender": "blender_advanced",
        "roblox": "roblox_parkour",
    }
    return broad_map.get(keys[0], keys[0])


def _find_lesson_number(text: str) -> Optional[int]:
    number = extract_lesson_number(text)
    if number == "":
        return None
    try:
        return int(number)
    except Exception:
        return None


def resolve_lesson_from_request(text: str) -> Optional[LessonTopic]:
    n = _canonical_report_query(text)
    course_key = _find_course_key(n)
    lesson_num = _find_lesson_number(n)
    if course_key is None or lesson_num is None:
        return None
    lessons = LESSON_TOPICS.get(course_key) or {}
    if lesson_num in lessons:
        return lessons[lesson_num]
    return None


