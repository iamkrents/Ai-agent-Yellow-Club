def report_variant_from_instruction(instruction: str) -> str:
    """Map teacher button/reply instruction to a stable report variant."""
    n = normalize(instruction or "")
    if any(w in n for w in ["короч", "сократ", "кратко", "short"]):
        return "short"
    if any(w in n for w in ["подроб", "детальн", "информатив", "раскрой", "full"]):
        return "detailed"
    if any(w in n for w in ["мягч", "тепл", "добр", "спокойн"]):
        return "soft"
    if any(w in n for w in ["передел", "другой", "вариант", "заново", "лучше"]):
        return "alternate"
    return "normal"


