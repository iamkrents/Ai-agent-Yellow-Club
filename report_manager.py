def is_parent_report_request(text: str) -> bool:
    n = _canonical_report_query(text)
    return (
        any(_canonical_report_query(marker) in n for marker in REPORT_REQUEST_MARKERS)
        or query_has_report_intent(text)
        or n.startswith("/parent_report")
        or n.startswith("/report_parent")
        or n.startswith("/report ")
    )


def strip_report_command(text: str) -> str:
    raw = (text or "").strip()
    lowered = raw.lower()
    for prefix in ["/parent_report", "/report_parent", "/report"]:
        if lowered.startswith(prefix):
            return raw[len(prefix):].strip(" :—-\n\t")
    n = _canonical_report_query(raw)
    # Remove common leading phrases without damaging lesson details.
    replacements = [
        "сделай отчет для родителей", "сделай отчет родителям", "сделай отчет в родительский чат",
        "сделай отчет", "составь отчет для родителей", "составь отчет родителям", "составь отчет",
        "напиши отчет для родителей", "напиши отчет родителям", "напиши отчет",
        "сообщение родителям по занятию", "сообщение в чат родителей", "отчет родителям", "отчет для родителей",
    ]
    for rep in replacements:
        rep_n = _canonical_report_query(rep)
        if n.startswith(rep_n):
            # We cannot cut by normalized length safely if latin words were changed;
            # remove the same phrase approximately from the raw beginning.
            raw_words = raw.split()
            rep_words = rep.split()
            if len(raw_words) >= len(rep_words):
                return " ".join(raw_words[len(rep_words):]).strip(" :—-\n\t") or raw.strip(" :—-\n\t")
    return raw.strip(" :—-\n\t")


