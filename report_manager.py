def _alternate_items(lesson: LessonTopic) -> list[str]:
    items = list(lesson.done)
    out: list[str] = []
    for item in items:
        cleaned = item.strip().rstrip('.')
        if cleaned.startswith("познакомились"):
            cleaned = cleaned.replace("познакомились", "разобрали", 1)
        elif cleaned.startswith("научились"):
            cleaned = cleaned.replace("научились", "потренировались", 1)
        elif cleaned.startswith("работали"):
            cleaned = cleaned.replace("работали", "продолжили работу", 1)
        elif cleaned.startswith("использовали"):
            cleaned = cleaned.replace("использовали", "применили", 1)
        elif cleaned.startswith("добавили"):
            cleaned = cleaned.replace("добавили", "настроили и добавили", 1)
        out.append(cleaned)
    return out or items


def _unique_items(items: list[str]) -> list[str]:
    seen = set()
    result: list[str] = []
    for item in items:
        cleaned = " ".join((item or "").strip().rstrip('.').split())
        if not cleaned:
            continue
        key = normalize(cleaned)
        if key in seen:
            continue
        seen.add(key)
        result.append(cleaned)
    return result


def _report_html(text: str) -> str:
    return html.escape((text or "").strip(), quote=False)


def _format_lesson_report_html(lesson: LessonTopic, items: list[str], learned: str, *, variant: str = "normal", extra: str = "") -> str:
    items = _unique_items(items)
    if variant == "short":
        items = items[:4]
    elif variant == "detailed":
        items = items[:7]
    else:
        items = items[:6]

    bullet_lines = "\n".join(f"• {_report_html(item)}" for item in items)
    learned = _report_html(learned.rstrip("."))
    extra = _report_html(extra.rstrip(".")) if extra else ""

    if variant == "soft":
        title_line = f"Сегодня ребята спокойно и последовательно поработали с темой «{_report_html(lesson.title)}»."
    elif variant == "alternate":
        title_line = f"Сегодня продолжили практическую работу по теме «{_report_html(lesson.title)}»."
    else:
        title_line = "Сегодня на занятии мы:"

    lines = [
        "👋 Здравствуйте, уважаемые родители!",
        "",
        f"📌 {title_line}",
        bullet_lines,
        "",
        "✨ Итог занятия:",
        f"Благодаря этому ребята научились {learned}.",
    ]
    if extra:
        lines.extend(["", f"🌱 {extra}."])
    return clean_report_output("\n".join(lines))


def build_report_from_lesson(lesson: LessonTopic, variant: str = "normal") -> str:
    variant = (variant or "normal").strip().lower()
    base_items = list(lesson.done)

    if variant == "short":
        return _format_lesson_report_html(lesson, base_items[:3], lesson.learned, variant="short")

    if variant == "detailed":
        items = base_items + [_concept_context_item(lesson), _practice_focus_item(lesson)]
        return _format_lesson_report_html(
            lesson,
            items,
            lesson.learned,
            variant="detailed",
            extra=_course_context_sentence(lesson),
        )

    if variant == "soft":
        return _format_lesson_report_html(
            lesson,
            base_items,
            lesson.learned,
            variant="soft",
            extra="Это поможет ребятам увереннее двигаться дальше в следующих практических работах",
        )

    if variant == "alternate":
        return _format_lesson_report_html(
            lesson,
            _alternate_items(lesson),
            lesson.learned,
            variant="alternate",
            extra=_course_context_sentence(lesson),
        )

    return _format_lesson_report_html(lesson, base_items, lesson.learned, variant="normal")
